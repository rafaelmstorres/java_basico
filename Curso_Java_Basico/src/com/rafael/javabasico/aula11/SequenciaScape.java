package com.rafael.javabasico.aula11;

public class SequenciaScape {

	public static void main(String[] args) {
		
		//imprimir "Hello World" com aspas simples e duplas
		System.out.println("\"Hello World\"");
		System.out.print("\'Hello World\'\n\r"); // \n pula para a lina de baixo e \r salta uma linha
		
		//imprimir 1\4
		System.out.print("1\\4");

	}

}
