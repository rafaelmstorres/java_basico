package com.rafael.javabasico.aula11;

public class VariaveisDoubleFloat {

	public static void main(String[] args) {
		
		double passagem = 3.80;
		
		float tomate = 4.99f;
		
		System.out.println("Preço da passagem: R$ " + passagem);
		
		System.out.println("Preço do kilo do tomate: R$ " + tomate);
	}

}
